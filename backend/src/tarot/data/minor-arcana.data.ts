/**
 * Complete Minor Arcana deck - 56 cards
 */
export const MINOR_ARCANA: TarotCard[] = [
  ...CUPS,
  ...PENTACLES,
  ...SWORDS,
  ...WANDS,
];

/**
 * Get all cards of a specific suit
 */
export function getCardsBySuit(suit: Suit): TarotCard[] {
  return MINOR_ARCANA.filter(card => card.suit === suit);
}

/**
 * Get a Minor Arcana card by ID
 */
export function getMinorArcanaById(id: string): TarotCard | undefined {
  return MINOR_ARCANA.find(card => card.id === id);
}

/**
 * Get a Minor Arcana card by suit and number
 */
export function getMinorArcanaBySuitAndNumber(suit: Suit, number: number): TarotCard | undefined {
  return MINOR_ARCANA.find(card => card.suit === suit && card.number === number);
}
