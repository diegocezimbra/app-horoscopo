/**
 * Helper function to create card name
 */
function getCardName(number: number, suitPt: string): string {
  const courtCards: Record<number, string> = {
    11: 'Pajem',
    12: 'Cavaleiro',
    13: 'Rainha',
    14: 'Rei',
  };

  if (number === 1) return `As de ${suitPt}`;
  if (number >= 11) return `${courtCards[number]} de ${suitPt}`;
  return `${number} de ${suitPt}`;
}

